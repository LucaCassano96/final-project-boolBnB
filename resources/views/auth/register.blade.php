<script>
    const form = document.getElementById('registerForm');
    const emailInput = document.getElementById('email');
    const passwordInput = document.getElementById('password');
    const confirmPwInput = document.getElementById('password-confirm');

    form.addEventListener('submit', function (event) {
        event.preventDefault(); // Prevent the form from submitting

        if (!validateEmail() || !validatePassword() || !confirmPassword()) {

            return false; // Stop form submission if validation fails
        }
        console.log('prova');
        // If validation passes, submit the form
        this.submit();
    });

    function validateEmail() {

        const emailValue = emailInput.value.trim();

        if (emailValue === '') {
            alert('Inserire un indirizzo email');
            emailInput.focus();
            return false;
        }

        return true;
    }

    function validatePassword() {

        const passwordValue = passwordInput.value.trim();

        if (passwordValue === '') {
            alert('Inserire una password');
            passwordInput.focus();
            return false;
        }else if (passwordValue.length < 8) {
            alert('La password deve avere almeno 8 caratteri');
            passwordInput.focus();
            return false;
        }

        // Additional validation logic for name can be added here

        return true;
    }

    function confirmPassword() {

        const passwordValue = passwordInput.value.trim();
        const passwordConfirmValue = confirmPwInput.value.trim();

        if (passwordConfirmValue === '') {
            alert('Necessario confermare la password');
            confirmPwInput.focus();
            return false;
        }else{
            if (passwordConfirmValue !== passwordValue) {
                alert("Le password non coincidono");
            return false;
            }

            return true;
        }


    }

</script>

